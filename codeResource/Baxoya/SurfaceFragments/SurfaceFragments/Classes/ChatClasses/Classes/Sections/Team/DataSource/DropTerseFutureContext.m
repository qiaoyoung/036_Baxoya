Byte * IslandCenter_DataToCache(Byte *data) {
    int countSurface = data[0];
    int behindDelicate = data[1];
    Byte materialBorder = data[2];
    int hostCircleTower = data[3];
    if (!countSurface) return data + hostCircleTower;
    for (int i = hostCircleTower; i < hostCircleTower + behindDelicate; i++) {
        int value = data[i] - materialBorder;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[hostCircleTower + behindDelicate] = 0;
    return data + hostCircleTower;
}

NSString *StringFromIslandCenter_Data(Byte *data) {
    return [NSString stringWithUTF8String:(char *)IslandCenter_DataToCache(data)];
}
