Byte * HideDataToCache(Byte *data) {
    int storageArtifactSurf = data[0];
    int titleHappy = data[1];
    Byte stemListen = data[2];
    int instance = data[3];
    if (!storageArtifactSurf) return data + instance;
    for (int i = instance; i < instance + titleHappy; i++) {
        int value = data[i] + stemListen;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[instance + titleHappy] = 0;
    return data + instance;
}

