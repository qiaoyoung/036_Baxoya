Byte * Speed_DataToCache(Byte *data) {
    int wingSurface = data[0];
    int dark = data[1];
    Byte riverPanel = data[2];
    int orbitLeap = data[3];
    if (!wingSurface) return data + orbitLeap;
    for (int i = orbitLeap; i < orbitLeap + dark; i++) {
        int value = data[i] + riverPanel;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[orbitLeap + dark] = 0;
    return data + orbitLeap;
}

NSString *StringFromSpeed_Data(Byte *data) {
    return [NSString stringWithUTF8String:(char *)Speed_DataToCache(data)];
}
