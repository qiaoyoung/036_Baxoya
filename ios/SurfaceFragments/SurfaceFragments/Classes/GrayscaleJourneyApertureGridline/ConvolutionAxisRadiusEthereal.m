Byte * Bold_DataToCache(Byte *data) {
    int receive = data[0];
    int transformPower = data[1];
    Byte spot = data[2];
    int logicSteel = data[3];
    if (!receive) return data + logicSteel;
    for (int i = logicSteel; i < logicSteel + transformPower; i++) {
        int value = data[i] - spot;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[logicSteel + transformPower] = 0;
    return data + logicSteel;
}

NSString *StringFromBold_Data(Byte *data) {
    return [NSString stringWithUTF8String:(char *)Bold_DataToCache(data)];
}
